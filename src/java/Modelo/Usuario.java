package Modelo;

/**
 *
 * @author Noctics
 */
public class Usuario {
    int id;
    String nom;
    String ape;
    String correo;
    String contraseña;

    public Usuario() {
    }

    public Usuario(int id, String nom, String ape, String correo, String contraseña) {
        this.id = id;
        this.nom = nom;
        this.ape = ape;
        this.correo = correo;
        this.contraseña = contraseña;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getApe() {
        return ape;
    }

    public void setApe(String ape) {
        this.ape = ape;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    

}
